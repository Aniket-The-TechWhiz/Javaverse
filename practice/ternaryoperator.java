public class ternaryoperator {
    public static void main(String args[])
    {
        int a=5;
        System.out.println(a%2==0 ?"Even": "Odd");

        int num1=20;
        int num2=10;
        System.out.println(num1>num2 ? num1+" is greater than "+num2: num2+" is greater than "+num1);
    }
}
